<h3>Janvāris</h3>
<ul>
    <li><strong>1. janvāris</strong>: Jaungada diena. Gads sākas ar brīvdienas noskaņām, atzīmējot jaunā gada iestāšanos.</li>
</ul>

<h3>Februāris</h3>
<ul>
    <li>Februārī bez papildu brīvdienām.</li>
</ul>

<h3>Marts</h3>
<ul>
    <li><strong>28. marts </strong>: Pirmsvētku diena ar īsāku, 7 stundu darba dienu.</li>
    <li><strong>29. marts </strong>: Lielā piektdiena, sākas Lieldienu brīvdienas.</li>
    <li><strong>31. marts </strong>: Pirmās Lieldienas.</li>
</ul>

<h3>Aprīlis</h3>
<ul>
    <li><strong>1. aprīlis</strong>: Otrās Lieldienas.</li>
    <li><strong>30. aprīlis</strong>: Pirmsvētku diena, arī šajā dienā ir 7 stundu darba diena.</li>
</ul>

<h3>Maijs</h3>
<ul>
    <li><strong>1. maijs</strong>: Darba svētki un Latvijas Republikas Satversmes sapulces sasaukšanas diena.</li>
    <li><strong>3. maijs</strong>: Pirmsvētku diena ar 7 stundu darba laiku.</li>
    <li><strong>4. maijs</strong>: Latvijas Republikas Neatkarības deklarācijas pasludināšanas diena.</li>
    <li><strong>6. maijs</strong>: Pārceltā brīvdiena no 4. maija.</li>
    <li><strong>12. maijs</strong>: Mātes diena.</li>
    <li><strong>19. maijs</strong>: Vasarsvētki.</li>
</ul>

<h3>Jūnijs</h3>
<ul>
    <li><strong>23. jūnijs</strong>: Līgo diena.</li>
    <li><strong>24. jūnijs</strong>: Jāņu diena, tradicionāli svinēta ar ugunskuriem un dziesmām.</li>
</ul>

<h3>Jūlijs</h3>
<ul>
    <li>Jūlijā bez papildu brīvdienām.</li>
</ul>

<h3>Augusts</h3>
<ul>
    <li>Augustā bez papildu brīvdienām.</li>
</ul>

<h3>Septembris</h3>
<ul>
    <li>Septembrī bez papildu brīvdienām.</li>
</ul>

<h3>Oktobris</h3>
<ul>
    <li>Oktobrī bez papildu brīvdienām.</li>
</ul>

<h3>Novembris</h3>
<ul>
    <li><strong>18. novembris</strong>: Latvijas Republikas proklamēšanas diena.</li>
</ul>

<h3>Decembris</h3>
<ul>
    <li><strong>14. decembris</strong>: Valsts un pašvaldības iestādes pārceļ darba dienu no 23. decembra.</li>
    <li><strong>23. decembris</strong>: Pirmssvētku diena ar 7 stundu darba laiku. Valsts un pašvaldības iestādes pārceļ darba dienu uz 14. decembri.</li>
    <li><strong>24. decembris</strong>: Ziemassvētku vakars.</li>
    <li><strong>25. decembris</strong>: Pirmie Ziemassvētki.</li>
    <li><strong>26. decembris</strong>: Otrie Ziemassvētki.</li>
    <li><strong>28. decembris</strong>: Valsts un pašvaldības iestādes pārceļ darba dienu no 30. decembra.</li>
    <li><strong>30. decembris</strong>: Pirmssvētku diena ar 7 stundu darba laiku. Valsts un pašvaldības iestādes pārceļ darba dienu uz 28. decembri.</li>
    <li><strong>31. decembris</strong>: Gada noslēguma diena.</li>
</ul>
<p><strong>Kopsavilkums:</strong> 2024. gadā Latvijā ir dažādas valsts brīvdienas, kas sniedz iespēju atpūsties un svinēt nozīmīgus notikumus. Dažās brīvdienās ir īpašas tradīcijas, piemēram, Jāņu dienā un Ziemassvētkos.</p>
